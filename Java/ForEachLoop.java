import java.util.*;
public class ForEachLoop {
public static void main(String args[]){
    int [] marks = new int[5];
    marks[0] =10;
    marks[1] =20;
    marks[2] =30;
    marks[3] =40;
    marks[4] =50;

    for(int element : marks){
        System.out.println(element);
    }

}
}
    

