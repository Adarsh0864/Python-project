str = "Adarsh Mishra"

print(str[1:5])  #here the index 4 will not be included.

# we can also use slicing this way

print(str[:5]) #Here this will count from the index 0 to index 4 


#and lets say we wanna print the whole thing from a index then we will use:

print(str[0: len(str)]) # This will print the whole string




